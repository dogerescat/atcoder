#include <bits/stdc++.h>
using namespace std;

int main() {
    string s; cin >> s;
    vector<int> x(4);
    for(int i = 0; i < 4; i++) {
        x[i] = s[i] - '0';
    }
    if(x[0] + x[1] + x[2] + x[3] == 7) {
        cout << x[0] << '+' << x[1] << '+' << x[2] << '+' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] - x[1] + x[2] + x[3] == 7) {
        cout << x[0] << '-' << x[1] << '+' << x[2] << '+' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] + x[1] - x[2] + x[3] == 7) {
        cout << x[0] << '+' << x[1] << '-' << x[2] << '+' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] + x[1] + x[2] - x[3] == 7) {
        cout << x[0] << '+' << x[1] << '+' << x[2] << '-' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] - x[1] - x[2] + x[3] == 7) {
        cout << x[0] << '-' << x[1] << '-' << x[2] << '+' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] - x[1] + x[2] - x[3] == 7) {
        cout << x[0] << '-' << x[1] << '+' << x[2] << '-' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] + x[1] - x[2] - x[3] == 7) {
        cout << x[0] << '+' << x[1] << '-' << x[2] << '-' << x[3] << '=' << '7' << endl;
        return 0;
    }
    if(x[0] - x[1] - x[2] - x[3] == 7) {
        cout << x[0] << '-' << x[1] << '-' << x[2] << '-' << x[3] << '=' << '7' << endl;
        return 0;
    }
}